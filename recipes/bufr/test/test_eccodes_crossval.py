"""Cross-validation tests comparing earth2bufr against eccodes references.

Each ``.bufr`` fixture in ``test/data/`` has a companion
``.eccodes.ref.json`` generated by ``generate_eccodes_references.py``.
These tests decode the same BUFR file with earth2bufr and compare the
output value-by-value against the eccodes reference, using FXY descriptor
codes as the bridge between the two naming schemes.
"""

from __future__ import annotations

import gzip
import json
import math
from pathlib import Path
from typing import TYPE_CHECKING

import pytest

if TYPE_CHECKING:
    import pyarrow as pa  # type: ignore[import-untyped]

try:
    import eccodes  # noqa: F401

    HAS_ECCODES = True
except ImportError:
    HAS_ECCODES = False


DATA_DIR = Path(__file__).parent / "data"

# eccodes uses very large negative sentinel for BUFR missing values
_ECCODES_MISSING_THRESHOLD = -1e30

